     * Display the specified resource.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        $item = User::with('favorite.shop.reserve.review', 'favorite.shop.area', 'favorite.shop.genre', 'favorite.shop.favorite', 'reserve.shop.area', 'reserve.shop.genre', 'reserve.review', 'shop.area', 'shop.genre')->where('id', $user->id)->get();
